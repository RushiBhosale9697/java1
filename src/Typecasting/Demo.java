package Typecasting;

public class Demo {

	public static void main(String[] args) {
		                 //integer to byte
		int a=20;
		byte b=10;
		System.out.println(a+b);
		int c=a+b;
		System.out.println(c);
		
		byte d=(byte) (a+b);
		System.out.println(d);
		
		//string to int
		String e="10";
		String f="20";

	}

}
